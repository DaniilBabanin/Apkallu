#!/usr/bin/env python3
"""Regenerate results/INDEX.md — one glanceable row per session, newest first.

Auto-runs at the end of every run_session.py (dispatch.py calls that per job), so the index
stays current with zero ceremony. Run it by hand only to force a refresh or after manual edits:

    python3 evals/agentic/index.py

Also backfills final.md (the agent's verdict/answer, pulled out of the bulky trajectory)
for older runs that predate run_session.py writing it. Idempotent; safe to re-run.
"""
import json
import os

from run_session import _extract_final

HERE = os.path.dirname(os.path.abspath(__file__))
RESULTS = os.path.join(HERE, "results")


def task_title(d):
    p = os.path.join(d, "task.md")
    if not os.path.exists(p):
        return ""
    for line in open(p):
        s = line.strip().lstrip("# ").strip()
        if s:
            return s[:70]
    return ""


def row(name):
    d = os.path.join(RESULTS, name)
    meta = json.load(open(os.path.join(d, "meta.json")))
    headline = meta.get("final_headline", "")
    traj = os.path.join(d, "trajectory.json")
    if not headline and os.path.exists(traj):                 # backfill older runs
        full, headline = _extract_final(traj)
        if full:
            open(os.path.join(d, "final.md"), "w").write(full)
            meta["final_headline"] = headline
            json.dump(meta, open(os.path.join(d, "meta.json"), "w"), indent=2)
    return {
        "id": meta.get("id", name),
        "model": (meta.get("model") or "").split("/")[-1],
        "task": task_title(d) or meta.get("task", "")[:70],
        "status": f'{meta.get("status","?")}/{meta.get("reason","")}'.rstrip("/"),
        "sec": meta.get("elapsed_sec", ""),
        "headline": (headline or "—").replace("|", "\\|")[:90],
    }


def main():
    names = sorted((n for n in os.listdir(RESULTS)
                    if os.path.exists(os.path.join(RESULTS, n, "meta.json"))), reverse=True)
    rows = []
    for n in names:
        try:
            rows.append(row(n))
        except Exception as e:                                # one bad run mustn't kill the index
            print(f"[index] skip {n}: {e}")
    out = ["# Results index", "",
           f"_Auto-generated by `index.py` — {len(rows)} sessions. Do not hand-edit; re-run to refresh._",
           "", "Curated cross-run insights live in `LEARNINGS.md`. Per-run answer/verdict is in each "
           "run's `final.md`; full reasoning in `trajectory.json`.", "",
           "| id | model | task | status | sec | answer / verdict |",
           "|----|-------|------|--------|----:|------------------|"]
    for r in rows:
        out.append(f'| {r["id"]} | {r["model"]} | {r["task"]} | {r["status"]} | {r["sec"]} | {r["headline"]} |')
    open(os.path.join(RESULTS, "INDEX.md"), "w").write("\n".join(out) + "\n")
    print(f"[index] wrote INDEX.md ({len(rows)} sessions)")


if __name__ == "__main__":
    main()
